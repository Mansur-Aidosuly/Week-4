import math

# Example 1 – sqrt()
print("Square root of 16:", math.sqrt(16))

# Example 2 – ceil()
print("Ceil of 4.3:", math.ceil(4.3))

# Example 3 – floor()
print("Floor of 4.9:", math.floor(4.9))

# Example 4 – sin() and cos() (angle in radians)
angle = math.pi / 2
print("sin(pi/2):", math.sin(angle))
print("cos(pi/2):", math.cos(angle))

# Example 5 – Constants pi and e
print("Value of pi:", math.pi)
print("Value of e:", math.e)