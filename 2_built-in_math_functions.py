# Example 1 – min()
numbers = [5, 2, 9, -1, 7]
print("Min:", min(numbers))

# Example 2 – max()
print("Max:", max(numbers))

# Example 3 – abs()
print("Absolute value of -10:", abs(-10))

# Example 4 – round()
print("Round 3.14159 to 2 decimals:", round(3.14159, 2))

# Example 5 – pow()
print("2^5:", pow(2, 5))
print("2^5 mod 3:", pow(2, 5, 3))  # modular exponentiation